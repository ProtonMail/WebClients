export const PLANS_MAP: PlansMap = deepFreeze(
    plans.reduce((acc, plan) => {
        acc[plan.Name] = plan;
        return acc;
    }, {} as PlansMap)
);

function multiplyPricing(pricing: Pricing, factor: number): Pricing {
    return Object.keys(pricing).reduce((acc, key) => {
        acc[+key as CYCLE] = (pricing[+key as CYCLE] ?? 0) * factor;
        return acc;
    }, {} as Pricing);
}

export function getTestPlans(currency?: Currency): Plan[] {
    if (!currency) {
        return plans;
    }

    return plans.map((plan) => {
        return {
            ...plan,
            Currency: currency,
            Pricing: isRegionalCurrency(currency) ? multiplyPricing(plan.Pricing, 2) : plan.Pricing,
            DefaultPricing:
                isRegionalCurrency(currency) && plan.DefaultPricing
                    ? multiplyPricing(plan.DefaultPricing, 2)
                    : plan.DefaultPricing,
        };
    });
}

export function getLongTestPlans(): Plan[] {
    return [...getTestPlans('USD'), ...getTestPlans('CHF'), ...getTestPlans('EUR'), ...getTestPlans('BRL')];
}
